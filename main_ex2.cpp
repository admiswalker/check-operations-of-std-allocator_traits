template <class T, class T_alloc_e>
class vec{
    T* pT;
    uint ttSize;
    T_alloc_e aE_state; // current state of T_alloc_e allocation.
    
public:
    vec(uint size){
        ttSize = size;

        pT = aE_state.allocate(ttSize); // memory allocation
        for(uint i=0; i<ttSize; ++i){ aE_state.construct(&pT[i]); } // call coustructor
    }
    ~vec(){
        for(uint i=0; i<ttSize; ++i){ aE_state.destroy(&pT[i]); } // call destructor
        aE_state.deallocate(pT, ttSize); // release memory
    }
};

